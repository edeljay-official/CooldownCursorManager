function CCM:DeepCopy(orig)
  local copy
  if type(orig) == "table" then
    copy = {}
    for k, v in pairs(orig) do
      copy[self:DeepCopy(k)] = self:DeepCopy(v)
    end
    setmetatable(copy, self:DeepCopy(getmetatable(orig)))
  else
    copy = orig
  end
  return copy
end
